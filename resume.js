function snapshotKorean() {
  koSnapshots = {};
  document.querySelectorAll("[data-key]").forEach(el => {
    koSnapshots[el.getAttribute("data-key")] = el.innerHTML;
  });
}

function restoreKorean() {
  if (!koSnapshots) return;
  document.querySelectorAll("[data-key]").forEach(el => {
    const key = el.getAttribute("data-key");
    if (koSnapshots[key] !== undefined) el.innerHTML = koSnapshots[key];
  });
}

function setLang(lang) {
  if (lang === "ko") {
    restoreKorean();
    return;
  }
  const dict = i18n[lang];
  if (!dict) return;

  document.querySelectorAll("[data-key]").forEach(el => {
    const key = el.getAttribute("data-key");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
}

